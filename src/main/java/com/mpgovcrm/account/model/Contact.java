package com.mpgovcrm.account.model;

public class Contact {
	
	private String colName ;
	
	private String colDesc;
	
	private String colNameA;
	
	private String colNameB;
	
	private String operation;
	
	private String colNameVal;
	
	private Double totalSum;
	
	public Contact() {
	}

	public String getColName() {
		return colName;
	}

	public void setColName(String colName) {
		this.colName = colName;
	}

	public String getColDesc() {
		return colDesc;
	}

	public void setColDesc(String colDesc) {
		this.colDesc = colDesc;
	}

	public String getColNameA() {
		return colNameA;
	}

	public void setColNameA(String colNameA) {
		this.colNameA = colNameA;
	}

	public String getColNameB() {
		return colNameB;
	}

	public void setColNameB(String colNameB) {
		this.colNameB = colNameB;
	}

	public String getOperation() {
		return operation;
	}

	public void setOperation(String operation) {
		this.operation = operation;
	}

	public String getColNameVal() {
		return colNameVal;
	}

	public void setColNameVal(String colNameVal) {
		this.colNameVal = colNameVal;
	}

	public Double getTotalSum() {
		return totalSum;
	}

	public void setTotalSum(Double totalSum) {
		this.totalSum = totalSum;
	}

	

	
	// Getter and Setter methods
	
}
